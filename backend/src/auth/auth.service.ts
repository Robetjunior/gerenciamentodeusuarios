import { Injectable, UnauthorizedException } from '@nestjs/common';
import { JwtService } from '@nestjs/jwt';
import { UsersService } from 'src/user/users.service';
import * as bcrypt from 'bcryptjs';

@Injectable()
export class AuthService {
  constructor(
    private usersService: UsersService,
    private jwtService: JwtService
  ) {}

  async validateUser(email: string, pass: string) {
    const user = await this.usersService.findByEmail(email);
    console.log('[LOGIN DEBUG]', { email, foundUser: user });

    const passwordMatches = user && await bcrypt.compare(pass, user.password);
    console.log('[LOGIN DEBUG] Senha confere?', passwordMatches);

    if (user && passwordMatches) {
      const { password, ...result } = user;
      return result;
    }

    return null;
  }

  async login(user: any) {
    const payload = { email: user.email, sub: user.id, role: user.role };
    return {
      access_token: this.jwtService.sign(payload, { expiresIn: process.env.JWT_EXPIRATION }),
      user,
    };
  }
}
